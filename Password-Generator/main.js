// Get a random value from the array

function getRandomValue(array) {
  return array[Math.floor(Math.random() * array.length)];
}

// Generate 4 passwords of 15 characters long and display them in the specific id's

function generate() {
  for (let i = 1; i <= 4; i++) {
    let password = "";
    for (let count = 0; count < 15; count++) {
      const rand = getRandomValue(values);
      password += rand;
    }
    let passwordInfo = document.getElementById("password-box" + i);
    passwordInfo.textContent = password;
  }
}
